package Exercise;

public class Pg_161_10 {
    public static void main(String[] args) {
        char[] alpha = {'a', 'b', 'c', 'd'};
        for (int i = 0; i < alpha.length; i++)
            System.out.print(alpha[i]);

        System.out.println();
        for (char c : alpha)
            System.out.print(c);
    }
}
