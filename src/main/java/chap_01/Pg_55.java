package chap_01;
/*
1번 문제) 화면에 다음과 같이 "Welcome!!", "자바 세계로 오신 것을 환영합니다" 를 출력하는 자바 프로그램을 작성하라.
ex) Welcome!!
    자바 세계로 오신 것을 환영합니다.

2번 문제) 화면에 "Sorry~~", "재밌어서 미안합니다~~" 을 출력하는 자바 프로그램을 작성하라.
ex) Sorry~~
    재밌어서 미안합니다~~
 */
public class Pg_55 {
    public static void main(String[] args){
        System.out.println("1번 문제)");
        System.out.println("Welcome!!");
        System.out.println("자바 세계로 오신 것을 환영합니다.");

        System.out.println("");     //아무 문자가 없는 "" 일지라도 프로그램은 인식하고 적지 않고 줄을 건너 뛴다. (println)

        System.out.println("2번 문제)");
        System.out.println("Sorry~~");
        System.out.println("재밌어서 미안합니다~~");
    }
}
